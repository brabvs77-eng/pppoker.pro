/**
 * Shared redirect rules for Cloudflare Pages `_redirects` (and legacy hosts).
 * Sources are path-only (no trailing slash except `/`).
 */

function normalizeSource(route) {
  if (route === '/') return '/';
  return route.replace(/\/$/, '');
}

function normalizeDestination(route) {
  if (route === '/') return '/';
  return route.replace(/\/$/, '') || '/';
}

import {
  collectTaxonomyBlogRedirects,
} from './taxonomy-blog-redirects.mjs';

/** @returns {{ static: Array<{source: string, destination: string}>, wildcards: Array<{source: string, destination: string}> }} */
export function collectRedirects(manifest) {
  const staticRedirects = [
    { source: '/ru', destination: '/' },
    { source: '/user-agreement', destination: '/en/user-agreement' },
    { source: '/privacy-policy', destination: '/en/privacy-policy' },
    { source: '/tag/pppoker-2', destination: '/tag/pppoker' },
    { source: '/__qs', destination: '/' },
    {
      source: '/mastermega-content/mastermega-content-megamenu-menuitem',
      destination: '/',
    },
    // HY/TJ have no native blog export — send legacy URLs to RU archive.
    { source: '/hy/blog', destination: '/blog' },
    { source: '/tj/blog', destination: '/blog' },
  ];

  const wildcards = [
    { source: '/ru/*', destination: '/:splat' },
    { source: '/hy/blog/*', destination: '/blog/:splat' },
    { source: '/tj/blog/*', destination: '/blog/:splat' },
  ];

  const seen = new Set(staticRedirects.map((entry) => entry.source));

  for (const page of manifest.pages) {
    if (!page.isRedirect || !page.redirectTo) continue;

    const source = normalizeSource(page.route);
    const destination = normalizeDestination(page.redirectTo);

    if (source === destination) continue;
    if (seen.has(source)) continue;

    seen.add(source);
    staticRedirects.push({ source, destination });
  }

  for (const { source, destination } of collectTaxonomyBlogRedirects(manifest)) {
    if (seen.has(source)) continue;
    seen.add(source);
    staticRedirects.push({ source, destination });
  }

  staticRedirects.sort((a, b) => a.source.localeCompare(b.source));

  return { static: staticRedirects, wildcards };
}

/** Cloudflare Pages `_redirects` format (space-separated, explicit 301). */
export function formatCloudflareRedirects({ static: staticRedirects, wildcards }) {
  const lines = [
    '# Generated by scripts/sync-redirects.mjs — do not edit by hand.',
    '# Cloudflare Pages: https://developers.cloudflare.com/pages/configuration/redirects/',
    '',
  ];

  // Wildcards after static rules; /ru before /ru/* is ensured by sort + separate entries.
  for (const { source, destination } of staticRedirects) {
    lines.push(`${source} ${destination} 301`);
  }

  for (const { source, destination } of wildcards) {
    lines.push(`${source} ${destination} 301`);
  }

  return `${lines.join('\n')}\n`;
}

/** Parse `_redirects` back into comparable structures for verification. */
export function parseCloudflareRedirects(content) {
  const staticRedirects = new Map();
  const wildcards = [];

  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;

    const match = trimmed.match(/^(\S+)\s+(\S+)(?:\s+(\d+))?$/);
    if (!match) continue;

    const [, source, destination] = match;
    if (source.includes('*')) {
      wildcards.push({ source, destination });
    } else {
      staticRedirects.set(source, destination);
    }
  }

  return { static: staticRedirects, wildcards };
}

export function expectedRedirectMaps(manifest) {
  const collected = collectRedirects(manifest);
  return {
    static: new Map(collected.static.map((entry) => [entry.source, entry.destination])),
    wildcards: collected.wildcards,
  };
}
