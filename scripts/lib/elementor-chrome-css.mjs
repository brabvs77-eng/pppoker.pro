/** Build Elementor chrome hide-rules from apps/web/src/config/elementor-chrome.json */

function elementClass(id) {
  return `.elementor-element-${id}`;
}

export function buildChromeOverridesCss(chrome) {
  const blogHide = chrome.legacyBlogSectionIds
    .map((id) => `#wordpress-page-root[data-hide-legacy-blog] ${elementClass(id)}`)
    .join(',\n');

  const spacers = chrome.legacyHeaderSpacerElementIds
    .map((id) => `#wordpress-page-root[data-native-chrome] ${elementClass(id)}`)
    .join(',\n');

  const promoCtas = chrome.homepageDuplicateCtaElementIds
    .map((id) => `#wordpress-page-root[data-home-promo] ${elementClass(id)}`)
    .join(',\n');

  return `/* Generated by scripts/emit-chrome-css.mjs — do not edit manually. */

${blogHide} {
  display: none !important;
}

#wordpress-page-root[data-native-chrome] .${chrome.duplicateManagerMenuItemClass} {
  display: none !important;
}

#wordpress-page-root[data-native-chrome] #masthead {
  min-height: 0;
  padding: 0;
  margin: 0;
  border: 0;
}

#wordpress-page-root[data-home-promo] ${elementClass(chrome.homepageHeroRootElementId)} {
  padding-top: 0 !important;
}

${spacers} {
  display: none !important;
}

${promoCtas} {
  display: none !important;
}

#wordpress-page-root[data-native-chrome] .custom-lang-switcher,
#wordpress-page-root[data-native-chrome] .wpml-ls-statics-footer {
  display: none !important;
}

#wordpress-page-root[data-native-chrome]
  #masthead
  .elementor[data-elementor-post-type='elementor-hf'] {
  display: none !important;
}

#wordpress-page-root[data-native-chrome]
  #masthead
  > .elementor[data-elementor-type='section'] {
  display: none !important;
}
`;
}
